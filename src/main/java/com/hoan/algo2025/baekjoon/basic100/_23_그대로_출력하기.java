import java.io.*;
import java.util.Scanner;

public class _23_그대로_출력하기 {
    public static void main(String[] args) throws IOException {
        Scanner sc = new Scanner(System.in);
        while (sc.hasNextLine()) System.out.println(sc.nextLine());
    }
}
