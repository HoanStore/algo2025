package com.hoan.algo2025.baekjoon.basic100;

import java.util.Scanner;

public class _9_기찍_N {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int N = input.nextInt();

        for(int i=N; i>0; i--){
            System.out.println(i);
        }

    }
}
